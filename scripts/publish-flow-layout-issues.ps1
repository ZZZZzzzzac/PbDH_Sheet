$ErrorActionPreference = "Stop"

function Find-Gh {
  $cmd = Get-Command gh -ErrorAction SilentlyContinue
  if ($cmd) {
    return $cmd.Source
  }

  $workspaceGh = Join-Path $PSScriptRoot "..\tools\gh\bin\gh.exe"
  if (Test-Path $workspaceGh) {
    return (Resolve-Path $workspaceGh).Path
  }

  $programFilesGh = "C:\Program Files\GitHub CLI\gh.exe"
  if (Test-Path $programFilesGh) {
    return $programFilesGh
  }

  $programFilesX86Gh = "C:\Program Files (x86)\GitHub CLI\gh.exe"
  if (Test-Path $programFilesX86Gh) {
    return $programFilesX86Gh
  }

  throw "GitHub CLI was not found. Install it first, then run this script again."
}

function New-TempBodyFile([string]$Body) {
  $path = Join-Path ([System.IO.Path]::GetTempPath()) ("pbdh-issue-" + [System.Guid]::NewGuid().ToString("N") + ".md")
  Set-Content -LiteralPath $path -Value $Body -Encoding UTF8
  return $path
}

function Create-Issue([string]$Title, [string]$Body) {
  $bodyFile = New-TempBodyFile $Body
  try {
    & $Gh issue create --repo $Repo --title $Title --body-file $bodyFile --label "enhancement" --label "needs-triage"
  }
  finally {
    Remove-Item -LiteralPath $bodyFile -Force -ErrorAction SilentlyContinue
  }
}

