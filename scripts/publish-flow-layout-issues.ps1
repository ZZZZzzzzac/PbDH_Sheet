$disclaimer

## Problem Statement

Authors can currently choose page, section, and module order, but actual Sheet Tool layout is mostly fixed by the Base Framework. Every section renders through one automatic grid, so Authors cannot declare rows, columns, column widths, module slot sizes, spacing, or basic layout styling in the System Package.

## Solution

Extend Flow Layout so Authors can declare layout structure and sizing in the System Package while keeping Sheet Renderer as the owner of layout rendering. Preserve existing `section.modules` packages and add `section.rows -> columns -> modules`.

## User Stories

1. As an Author, I want existing `section.modules` packages to keep working, so that old System Packages remain valid.
2. As an Author, I want to group Sheet Modules into rows and columns, so that pages can express intentional structure.
3. As an Author, I want column widths and minimum widths, so that modules can have different visual weight and wrap on mobile.
4. As an Author, I want gap, margin, padding, background, border, radius, and alignment fields, so that System Packages can control density and grouping.
5. As a Player, I want customized layouts to remain responsive, so that the Sheet Tool works on desktop and mobile.

## Implementation Decisions

- Sheet Renderer remains responsible for Flow Layout.
- Support legacy `section.modules` and rich `section.rows[].columns[].modules`.
- Module placements can be strings or objects with `ID` and layout style.
- Layout styling is safe box styling, not arbitrary global CSS.
- Character Data remains keyed by module ID.

## Testing Decisions

- Test schema validation for legacy and rich Flow Layout.
- Test nested missing module references.
- Test renderer output for rows, columns, and module slots.
- Keep existing module interaction tests passing.

## Out of Scope

- Drag-and-drop layout editor.
- Overlay Layout.
- Arbitrary page-level Author CSS.
- Moving Sheet Module state into layout nodes.
"@

